public class Wizard {
    public void castSpell() {
        System.out.println("Wizard casts a spell!");
    }

    public void shield() {
        System.out.println("Wizard casts a shield spell!");
    }

    public void teleport() {
        System.out.println("Wizard teleports!");
    }
}


