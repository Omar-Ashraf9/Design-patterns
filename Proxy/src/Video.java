public class Video {
    private String title;
    private String data;

    public Video() {
    }

    public Video(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public String getData() {
        return data;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setData(String data) {
        this.data = data;
    }
}
